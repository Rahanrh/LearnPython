# logical operators

# and
print(5 > 3 and 4 > 1)   # return true because both condition is true
print(5 > 3 and 4 < 1)   # return False because one condition is false

# or
# return true if any one condition is true
print(10 < 5 or 5 > 3)
print(2 < 5 or 7 < 0)

# not
# reverse the answer

print(not(10 > 5))
