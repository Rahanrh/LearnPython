# Assign value to left operant (=)
num = 5
print(num)

# (+=)
num += 10  # num = num+10
print(num)

# (-=)
num -= 10  # num = num-10
print(num)

# (*=)
num *= 5  # num = num*5
print(num)

# (//=)
num //= 2  # num =num//2
print(num)

# (/=)
num /= 3  # num =num/3
print(num)

# (%=)
num %= 2  # num = num%2
print(num)
